public class MoreModel {

    String contactAndLicensingType;
    String title;
    String description;
    String url;
    String phone;
    String mobile;
    String subType;

    public String getContactAndLicensingType() {
        return contactAndLicensingType;
    }

    public void setContactAndLicensingType(String contactAndLicensingType) {
        this.contactAndLicensingType = contactAndLicensingType;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getSubType() {
        return subType;
    }

    public void setSubType(String subType) {
        this.subType = subType;
    }
}